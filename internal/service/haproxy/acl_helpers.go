func fetchACLModel(ctx context.Context, c *ophaproxy.Controller, id string) (aclResourceModel, error) {
	obj, err := c.HAProxyGetACL(ctx, id)
	if err != nil {
		return aclResourceModel{}, err
	}
	return aclResponseToModel(ctx, id, obj), nil
}

func findACLIDByName(ctx context.Context, c *ophaproxy.Controller, name string) (string, bool, error) {
	result, err := c.HAProxySearchACLs(ctx)
	if err != nil {
		return "", false, err
	}
	for _, row := range result.Rows {
		if apiValueToString(row["name"]) == name {
			return apiValueToString(row["uuid"]), true, nil
		}
	}
	return "", false, nil
}
